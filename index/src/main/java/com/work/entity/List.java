package com.work.entity;

public class List {
	private String id ;  //编号
	private String job_name ;  //工作名称
	private String salary ;  //薪资
	private String degree ;  //学历
	private String work_year ;  //工作时间
	private String job_area ;  //工作地区
	private String company_name ;  //公司名称
	private String company_size ;  //公司规模
	private String industry_type ;  //行业类型
	private String job_href ;  //工作链接
	private String company_href ;  //公司链接
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getJob_name() {
		return job_name;
	}
	public void setJob_name(String job_name) {
		this.job_name = job_name;
	}
	public String getSalary() {
		return salary;
	}
	public void setSalary(String salary) {
		this.salary = salary;
	}
	public String getDegree() {
		return degree;
	}
	public void setDegree(String degree) {
		this.degree = degree;
	}
	public String getWork_year() {
		return work_year;
	}
	public void setWork_year(String work_year) {
		this.work_year = work_year;
	}
	public String getJob_area() {
		return job_area;
	}
	public void setJob_area(String job_area) {
		this.job_area = job_area;
	}
	public String getCompany_name() {
		return company_name;
	}
	public void setCompany_name(String company_name) {
		this.company_name = company_name;
	}
	public String getCompany_size() {
		return company_size;
	}
	public void setCompany_size(String company_size) {
		this.company_size = company_size;
	}
	public String getIndustry_type() {
		return industry_type;
	}
	public void setIndustry_type(String industry_type) {
		this.industry_type = industry_type;
	}
	public String getJob_href() {
		return job_href;
	}
	public void setJob_href(String job_href) {
		this.job_href = job_href;
	}
	public String getCompany_href() {
		return company_href;
	}
	public void setCompany_href(String company_href) {
		this.company_href = company_href;
	}
	
}
